// Generate the render worker's bundled-font CSS: the seven video fonts as data-URL
// @font-face rules, written to render-worker/remotion/videoFontFaces.generated.ts so the
// FINAL render uses the exact same faces as the live preview (parity). Data URLs, not
// served files: the render bundle stays self-contained and the fonts load with no network
// (deterministic, and instant behind the render worker's delayRender gate).
//
// Runs in postinstall and again at the top of render-worker/bundle.mjs (deploy), so the
// generated module is always present and fresh for typecheck, `remotion studio`, and the
// render bundle. The file is gitignored - it is derived from public/fonts + videoFonts.ts.
//
//   node scripts/gen-video-font-css.mjs

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const { videoFontFaceCss } = await import(pathToFileURL(join(root, 'src', 'video', 'videoFonts.ts')).href);

const fontsDir = join(root, 'public', 'fonts');
const dataUrl = (file) => `data:font/woff2;base64,${readFileSync(join(fontsDir, file)).toString('base64')}`;
const css = videoFontFaceCss(dataUrl);

// This module carries the font BYTES, so it carries their licence too — the copyright notice
// and the OFL text as a human-readable header (OFL §2). Source: public/fonts/OFL.txt.
const oflHeader = readFileSync(join(root, 'src', 'assets', 'OFL.txt'), 'utf8')
  .split('\n')
  .map((line) => `// ${line}`.trimEnd())
  .join('\n');

const out = `// GENERATED by scripts/gen-video-font-css.mjs from public/fonts + src/video/videoFonts.ts.
// Do not edit; regenerate after changing the bundled fonts. The seven video fonts as
// data-URL @font-face rules, embedded so the render matches the preview (see videoFonts.ts).
//
${oflHeader}

export const VIDEO_FONT_FACE_CSS = ${JSON.stringify(css)};
`;

const target = join(root, 'render-worker', 'remotion', 'videoFontFaces.generated.ts');
writeFileSync(target, out);
console.log(`[video-fonts] wrote ${css.length} chars of @font-face CSS -> render-worker/remotion/videoFontFaces.generated.ts`);
